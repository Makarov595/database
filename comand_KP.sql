-- запрос количество скважин на КП в году
select  ogf.field_name,
		wp.platform_name as platform, 
		year(w.finish_drill_w_date) as year,
		count(distinct (w.well_number)) as total_wells 
	from wells as w 
		join well_platforms as wp
			on w.platform_id =wp.id
		join oil_gas_fields ogf
			on w.field_id =ogf.id
				where year(w.finish_drill_w_date) = 2020
					group by wp.platform_name, year(w.finish_drill_w_date);
		

	
		
-- запрос количество скважин по типам и средние сроки бурения в году	
select  ogf.field_name as field,
		year(w.finish_drill_w_date) as drill_year,
		wt.type_name as well_type,
		count(distinct (w.well_number)) as total_wells,
		ROUND(AVG(TIMESTAMPDIFF(minute, w.start_drill_w_date, w.finish_drill_w_date)/1440),2) as avg_drill_time 
	from wells as w 
		join well_platforms as wp
			on w.platform_id =wp.id
		join oil_gas_fields ogf
			on w.field_id =ogf.id
		join well_types wt
			on w.type_id = wt.id
				where year(w.finish_drill_w_date) = 2020
			group by wt.type_name;

		
		
		
-- запрос количество скважин по типам и средние сроки бурения в году	ЗИМНИЕ
select distinct wt.type_name as well_type,
		ROUND(AVG(TIMESTAMPDIFF(minute, w.start_drill_w_date, w.finish_drill_w_date)/1440) over w_t,1) as avg_drill_time,
		count(w.well_number) over w_t as total_wells
	from well_types as wt
		join wells as w 
			on w.type_id = wt.id
				where year(w.finish_drill_w_date) = 2020 and month(w.finish_drill_w_date) in (1,2,3,4,12)
	window w_t as(partition by wt.type_name);
					
		
-- запрос количество скважин по типам и средние сроки бурения в году	ЛЕТНИЕ
select distinct wt.type_name as well_type,
		ROUND(AVG(TIMESTAMPDIFF(minute, w.start_drill_w_date, w.finish_drill_w_date)/1440) over w_t,1) as avg_drill_time,
		count(w.well_number) over w_t as total_wells
	from well_types as wt
		join wells as w 
			on w.type_id = wt.id
				where year(w.finish_drill_w_date) = 2020 and month(w.finish_drill_w_date) in (5,6,7,8,9,10,11)
	window w_t as(partition by wt.type_name);
					

		
-- представление количество скважин по типам и средние сроки бурения в 2020 году
	DROP view IF EXISTS wells_types_2020;
	
create view  wells_types_2020 as
		select  ogf.field_name as field,
		year(w.finish_drill_w_date) as drill_year,
		wt.type_name as well_type,
		count(distinct (w.well_number)) as total_wells,
		ROUND(AVG(TIMESTAMPDIFF(minute, w.start_drill_w_date, w.finish_drill_w_date)/1440),1) as avg_drill_time 
	from wells as w 
		join well_platforms as wp
			on w.platform_id =wp.id
		join oil_gas_fields ogf
			on w.field_id =ogf.id
		join well_types wt
			on w.type_id = wt.id
				where year(w.finish_drill_w_date) = 2020
			group by wt.type_name
		order by wt.type_name;
	

