DROP DATABASE IF EXISTS grafffik;

CREATE DATABASE IF NOT EXISTS grafffik;

USE grafffik;



DROP TABLE IF EXISTS oil_gas_fields;
CREATE table IF NOT EXISTS oil_gas_fields 
		(
	id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
	field_name VARCHAR(255) NOT NULL COMMENT 'Название месторождения'
		);
		
DROP TABLE IF EXISTS well_platforms;
CREATE TABLE IF NOT EXISTS well_platforms 
		(
	id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
	platform_name VARCHAR(30) COMMENT 'Название куста',
	project_quantity_wells INT NOT NULL COMMENT 'проектное количество скважин',
	start_build_p_date DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT 'начало строительства КП',
	finish_build_p_date DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT 'финальное окончание строительства КП'
		);

DROP TABLE IF EXISTS wells;
CREATE TABLE  IF NOT EXISTS wells 
		(
	id INT UNSIGNED NOT NULL AUTO_INCREMENT,
	platform_id INT UNSIGNED NOT NULL COMMENT 'id куста',
	field_id INT UNSIGNED NOT NULL COMMENT 'id Месторождение',
	drill_team_id INT UNSIGNED NOT NULL COMMENT 'id буровой подрядчик/бригада',
	layer_id INT UNSIGNED NOT NULL COMMENT 'id пласта',
	design_id INT UNSIGNED NOT NULL COMMENT 'id конструкции скважины',
	status_id INT UNSIGNED NOT NULL COMMENT 'id статуса работ по скважине',
	well_number INT COMMENT 'Номер скважины',
	well_purpose VARCHAR(30) COMMENT 'Назначение скважины',
	well_function VARCHAR(10) COMMENT 'тип флюида',
	start_build_w_date DATETIME COMMENT 'начало строительства скв',
	finish_build_w_date DATETIME COMMENT 'окончание строительства скв',
	PRIMARY KEY (id)
		);
	
DROP TABLE IF EXISTS work_time;
CREATE TABLE work_time 
		(
	well_id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
	drill_time FLOAT NOT NULL COMMENT 'Время бурения',
	develop_time FLOAT NOT NULL COMMENT 'Время освоения',
	trial_time FLOAT NOT NULL COMMENT 'Время до ввода в эксплуатацию',
	TPD_time FLOAT NOT NULL COMMENT 'Время вышкомонтажных работ'
		);


DROP TABLE IF EXISTS well_project;
CREATE TABLE well_project 
		(
	id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
	design VARCHAR(255) COMMENT 'конструкция скважины',
		);
	

	
DROP TABLE IF EXISTS layers;
CREATE TABLE layers 
		(
	id INT UNSIGNED NOT NULL AUTO_INCREMENt PRIMARY KEY,
	name VARCHAR(30) COMMENT 'название пласта'
		);
	
DROP TABLE IF EXISTS drill_teams;
CREATE TABLE IF NOT EXISTS drill_teams 
		(
	id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
	company_name VARCHAR(255) COMMENT 'название подрядчика',
	brigade_number INT COMMENT 'номер бригады'
		);

	
DROP TABLE IF EXISTS well_statuses;
CREATE TABLE IF NOT EXISTS well_statuses 
		(
	id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
	status VARCHAR(30) COMMENT 'статус работ по скважине'
		);
	
	
-- создаем внешние ключи
ALTER TABLE wells 
	ADD CONSTRAINT wells_platform_id_fk
		FOREIGN KEY (platform_id) REFERENCES well_platforms(id)
			ON DELETE CASCADE;

ALTER TABLE wells 
	ADD CONSTRAINT wells_field_id_fk
		FOREIGN KEY (field_id) REFERENCES oil_gas_fields(id)
			ON DELETE CASCADE;
		
ALTER TABLE wells 
	ADD CONSTRAINT wells_drill_team_id_fk
		FOREIGN KEY (drill_team_id) REFERENCES drill_teams(id)
			ON DELETE CASCADE;

ALTER TABLE wells 
	ADD CONSTRAINT wells_layer_id_fk
		FOREIGN KEY (layer_id) REFERENCES layers (id)
			ON DELETE CASCADE;
		
ALTER TABLE wells 
	ADD CONSTRAINT wells_design_id_fk
		FOREIGN KEY (design_id) REFERENCES well_project (id)
			ON DELETE CASCADE;
	
ALTER TABLE work_time 
	ADD CONSTRAINT work_time_well_id_fk
		FOREIGN KEY (well_id) REFERENCES wells (id)
			ON DELETE CASCADE;
		
		
ALTER TABLE wells 
	ADD CONSTRAINT wells_status_id_fk
		FOREIGN KEY (status_id) REFERENCES well_statuses (id)
			ON DELETE CASCADE;
		

		
-- запрос инфо по кустам
select wp.platform_name, 
		count(w.well_number) as total_wells,
		SUM(wt.drill_time) as total_drill, 
		SUM(wt.develop_time) as total_develop, 
		SUM(wt.trial_time) as total_trial, 
		SUM(wt.TPD_time) as total_TPD,
		date_format(w.start_build_w_date, '%d/%m/%Y') as data_start, 
		date_format(w.finish_build_w_date , '%d/%m/%Y') as data_finish
	from wells as w 
		join well_platforms as wp
			on w.platform_id =wp.id
		join work_time as wt
			on w.id = wt.well_id
				group by wp.platform_name;


